//Lerning Loops with For
//Print numbers from 1 to 100

for(var i = 1; i <= 100; i++ ){
	console.log("Number 1: "+i);
}

for(var i = 1; i <= 100; i=i+5 ){
	console.log("Number 3: "+i);
}

var j=5;
while(j>5){
	console.log(j);
	j++;
}


